    private void AlteraModoOperacaoForm(ModoOperacaoForm estado) {
        Component[] compPnlDetalhe = pnlDetalhe.getComponents();
        switch(estado){
        case PADRAO:
            statusForm = ModoOperacaoForm.PADRAO;

            btnAlterar.setEnabled(true);
            btnExcluir.setEnabled(true);
            btnIncluir.setEnabled(true);
            btnPesquisar.setEnabled(true);
            btnCancelar.setEnabled(false);
            btnConfirmar.setEnabled(false);
            btnConfirmar.setText("Confirmar");
            btnPrimeiro.setEnabled(true);
            btnAnterior.setEnabled(true);
            btnProximo.setEnabled(true);
            btnUltimo.setEnabled(true);
            btnMostrarTodos.setEnabled(true);

            for (Component comp1 : compPnlDetalhe) {
                comp1.setEnabled(false);
            }

            tplPrincipal.setSelectedIndex(0);
            break;

        case PESQUISA:
            statusForm = ModoOperacaoForm.PESQUISA;

            btnAlterar.setEnabled(false);
            btnExcluir.setEnabled(false);
            btnIncluir.setEnabled(false);
            btnPesquisar.setEnabled(false);
            btnCancelar.setEnabled(true);
            btnConfirmar.setText("Pesquisar");
            btnConfirmar.setEnabled(true);
            btnPrimeiro.setEnabled(false);
            btnAnterior.setEnabled(false);
            btnProximo.setEnabled(false);
            btnUltimo.setEnabled(false);
            btnMostrarTodos.setEnabled(false);

            for (Component comp1 : compPnlDetalhe) {
                comp1.setEnabled(false);
            }

            toolbox.ResetPanelComponents(pnlDetalhe);
            ftxtDescricao.setEnabled(true);

            tplPrincipal.setSelectedIndex(1);
            ftxtDescricao.requestFocus();
            break;

        case INCLUSAO:
            statusForm = ModoOperacaoForm.INCLUSAO;

            btnAlterar.setEnabled(false);
            btnExcluir.setEnabled(false);
            btnIncluir.setEnabled(false);
            btnPesquisar.setEnabled(false);
            btnCancelar.setEnabled(true);
            btnConfirmar.setEnabled(true);
            btnConfirmar.setText("Confirmar");
            btnPrimeiro.setEnabled(false);
            btnAnterior.setEnabled(false);
            btnProximo.setEnabled(false);
            btnUltimo.setEnabled(false);
            btnMostrarTodos.setEnabled(false);

            for (Component comp1 : compPnlDetalhe) {
                comp1.setEnabled(true);
            }
            toolbox.ResetPanelComponents(pnlDetalhe);

            tplPrincipal.setSelectedIndex(1);
            ftxtDescricao.requestFocus();
            break;

        case EXCLUSAO:
            try {
                String aviso;
                if (tblMestre.getSelectedRow() != -1){
                    aviso = "Tem certeza que deseja excluir o serviço " + ftxtDescricao.getText() + " ?";
                    if (JOptionPane.showConfirmDialog(this, aviso, "Excluir serviço",
                            JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                        seLocal = new Servico(idServicoAtual);
                        seLocal.DeleteServico();
                        aviso = "Serviço excluído com sucesso!";
                        JOptionPane.showMessageDialog(this, aviso, "Excluir serviço", JOptionPane.INFORMATION_MESSAGE);
                    }
                }
                else {
                    aviso = "Nenhum serviço selecionado!\n\nClique em um dos serviços, para poder efetuar a exclusão!";
                    JOptionPane.showMessageDialog(this, aviso, "OPS!", JOptionPane.WARNING_MESSAGE);
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this,"Falha SQL! Código: " + e.getErrorCode() + 
                        ". Mensagem: " + e.getMessage(), "Ops!", JOptionPane.ERROR_MESSAGE);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this,"Falha genérica! Mensagem: " + e.getMessage(),
                        "Ops!", JOptionPane.ERROR_MESSAGE);
            } finally {
                PreencheTabelaPrincipal(queryPadrao);
                AlteraModoOperacaoForm(ModoOperacaoForm.PADRAO);    
            }
            break;
            
        case ALTERACAO:
            statusForm = ModoOperacaoForm.ALTERACAO;

            btnAlterar.setEnabled(false);
            btnExcluir.setEnabled(false);
            btnIncluir.setEnabled(false);
            btnPesquisar.setEnabled(false);
            btnCancelar.setEnabled(true);
            btnConfirmar.setEnabled(true);
            btnConfirmar.setText("Confirmar");
            btnPrimeiro.setEnabled(false);
            btnAnterior.setEnabled(false);
            btnProximo.setEnabled(false);
            btnUltimo.setEnabled(false);
            btnMostrarTodos.setEnabled(false);

            for (Component comp1 : compPnlDetalhe) {
                comp1.setEnabled(true);
            }
            
            seLocal = new Servico(idServicoAtual);
            
            tplPrincipal.setSelectedIndex(1);
            ftxtDescricao.requestFocus();
            
            break;
        }  
    }

    // Funções para validação de Dados
    private void ValidaCamposServico() throws Exception{
        if (!toolbox.ValidaFTextField(ftxtDescricao, false)){
            throw new Exception("Campo 'Nome' preenchido incorretamente!");
        }
        if (!toolbox.ValidaFTextField(ftxtValorCusto, false)){
            throw new Exception("Campo 'Valor de Custo' preenchido incorretamente!");
        }
        if (!toolbox.ValidaFTextField(ftxtValorServico, false)){
            throw new Exception("Campo 'Valor do Serviço' preenchido incorretamente!");
        }
    }
    
    private void PreencheTabelaPrincipal(String query){
        try {
            toolbox.LimparTabela(tblMestre);            
            DefaultTableModel tblMestreModel = sql.GetTableData(tableColumnsName,query,tblMestre);
            if (tblMestreModel.getRowCount() == 0) {
                lblNoDataFound.setVisible(true);
            } 
            else {
                lblRegistroTotal.setText(String.valueOf(tblMestreModel.getRowCount()));
                lblRegistroAtual.setText(String.valueOf(tblMestre.getSelectedRow() + 1));
                lblNoDataFound.setVisible(false);
            } 
            tblMestre.setModel(tblMestreModel);                       
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this,"Falha SQL! Código: " + e.getErrorCode() + 
                    ". Mensagem: " + e.getMessage(), "Ops!", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,"Falha genérica! Mensagem: " + e.getMessage(),
                    "Ops!", JOptionPane.ERROR_MESSAGE);
        }   
    }

    private void CarregaValoresPadrao() {
        try {
            PreencheTabelaPrincipal(queryPadrao);           
            lblUsuario.setText(GerenciadorViveiros.usuarioAtual.getCodigo());
        } catch (Exception e) {
           JOptionPane.showMessageDialog(this,"Falha! Mensagem: " + e.getMessage(),
                    "Ops!", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private void AtualizaCampos() {
        try {
                lblRegistroAtual.setText(String.valueOf(tblMestre.getSelectedRow() + 1));
                idServicoAtual = (int) tblMestre.getValueAt(tblMestre.getSelectedRow(), 0);
                
                ftxtDescricao.setText(String.valueOf(tblMestre.getValueAt(tblMestre.getSelectedRow(), 1)));
                ftxtValorServico.setText(String.valueOf(tblMestre.getValueAt(tblMestre.getSelectedRow(), 2)));
                if (String.valueOf(tblMestre.getValueAt(tblMestre.getSelectedRow(), 3)).equals(String.valueOf("Sim"))) {
                    chkPermiteProducao.setSelected(true);
                } else {
                    chkPermiteProducao.setSelected(false);
                }
                ftxtValorCusto.setText(String.valueOf(tblMestre.getValueAt(tblMestre.getSelectedRow(), 4)));
            } catch (Exception ex) {
                lblRegistroAtual.setText("0");
            }
    }
    
