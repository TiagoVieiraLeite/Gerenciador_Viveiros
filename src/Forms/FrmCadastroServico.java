    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        try {
            String query = "";
            String message = "";
            
            switch (statusForm) {
                /*
                    INCLUSÃO DE DADOS
                */
                case INCLUSAO:
                    ValidaCamposServico();                    
                    seLocal = new Servico(ftxtDescricao.getText(),
                            Double.valueOf(ftxtValorCusto.getText()),
                            Double.valueOf(ftxtValorServico.getText()),
                            chkPermiteProducao.isSelected());

                    seLocal.InsertServico();

                    message = "Serviço " + ftxtDescricao.getText() + " incluído com sucesso!";
                    JOptionPane.showMessageDialog(this, message, "Sucesso!", JOptionPane.INFORMATION_MESSAGE);
                    query = queryPadrao;
                    break;
                
                /*
                    PESQUISA DE DADOS
                */
                case PESQUISA:
                    if (!toolbox.ValidaTextField(ftxtDescricao, false)){
                        throw new Exception("Campo 'Nome' preenchido incorretamente!");
                    }
                    
                    query = "SELECT ID," +
                            "DESCRICAO," +
                            "VALORSERVICO," +
                            "CASE PERMITEPROD" +
                            " WHEN true THEN 'Sim'" +
                            " WHEN false THEN 'Não'" +
                            " END PERMITEPROD," +
                            "VALORCUSTO " +
                            "FROM SERVICO " +
                            "WHERE DESCRICAO LIKE '%" + 
                            ftxtDescricao.getText() + "%';";
                    
                    ftxtDescricao.setText("");
                    break;

                /*
                    PESQUISA DE DADOS
                */
                case ALTERACAO:
                    // Valida os campos
                    ValidaCamposServico();
                    message = "Tem certeza que deseja alterar o serviço " + ftxtDescricao.getText() + " ?";
                    if (JOptionPane.showConfirmDialog(this, message, "Alterar serviço",
                            JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
                        
                        seLocal = new Servico(idServicoAtual,
                                ftxtDescricao.getText(),
                                Double.valueOf(ftxtValorCusto.getText()),
                                Double.valueOf(ftxtValorServico.getText()),
                                chkPermiteProducao.isSelected());

                        seLocal.UpdateServico();
                        message = "Serviço " + ftxtDescricao.getText() + " alterado com sucesso!";
                        JOptionPane.showMessageDialog(this, message, "Alterar serviço", JOptionPane.INFORMATION_MESSAGE);
                    }
                    query = queryPadrao;
                    
                    break;
            }
            AlteraModoOperacaoForm(ModoOperacaoForm.PADRAO);
            PreencheTabelaPrincipal(query);
        } catch (Exception e){
            JOptionPane.showMessageDialog(this,"Falha ao efetuar operação! Mensagem: " + e.getMessage(),
                    "Ops!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnConfirmarActionPerformed

